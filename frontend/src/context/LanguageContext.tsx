const globalTextTranslations: TextTranslationMap = Object.assign({}, ...Object.values(textTranslationGroups));

const reverseGlobalTextTranslations = Object.fromEntries(
    Object.entries(globalTextTranslations).map(([english, albanian]) => [albanian, english]),
) as Record<string, string>;

type TranslationDictionary = (typeof pageTranslations)[Language];

interface LanguageContextValue {
    language: Language;
    setLanguage: (language: Language) => void;
    t: TranslationDictionary;
}

const LanguageContext = createContext<LanguageContextValue | undefined>(undefined);

export function LanguageProvider({ children }: { children: React.ReactNode }) {
    const [language, setLanguageState] = useState<Language>("sq");

    useEffect(() => {
        const stored = window.localStorage.getItem(STORAGE_KEY);
        if (stored === "sq" || stored === "en") {
            setLanguageState(stored);
        }
    }, []);

    const setLanguage = (nextLanguage: Language) => {
        setLanguageState(nextLanguage);
        if (typeof window !== "undefined") {
            window.localStorage.setItem(STORAGE_KEY, nextLanguage);
        }
    };

    const value = useMemo(
        () => ({
            language,
            setLanguage,
            t: pageTranslations[language],
        }),
        [language],
    );

    return (
        <LanguageContext.Provider value={value}>
            <GlobalTranslationLayer language={language} />
            {children}
        </LanguageContext.Provider>
    );
}

function GlobalTranslationLayer({ language }: { language: Language }) {
    useEffect(() => {
        document.documentElement.lang = language === "sq" ? "sq" : "en";
        translateDocument(language);

        let scheduled = false;
        const scheduleTranslation = () => {
            if (scheduled) return;
            scheduled = true;
            window.requestAnimationFrame(() => {
                scheduled = false;
                translateDocument(language);
            });
        };

        const observer = new MutationObserver(scheduleTranslation);
        observer.observe(document.body, {
            attributes: true,
            attributeFilter: ["placeholder", "aria-label", "title"],
            childList: true,
            characterData: true,
            subtree: true,
        });

        return () => observer.disconnect();
    }, [language]);

    return null;
}

function translateDocument(language: Language) {
    const walker = document.createTreeWalker(
        document.body,
        NodeFilter.SHOW_TEXT,
        {
            acceptNode(node) {
                const parent = node.parentElement;
                if (!parent) return NodeFilter.FILTER_REJECT;
                const tagName = parent.tagName.toLowerCase();
                if (["script", "style", "textarea", "input"].includes(tagName)) {
                    return NodeFilter.FILTER_REJECT;
                }
                if (!node.nodeValue?.trim()) {
                    return NodeFilter.FILTER_REJECT;
                }
                return NodeFilter.FILTER_ACCEPT;
            },
        },
    );

    const textNodes: Text[] = [];
    while (walker.nextNode()) {
        textNodes.push(walker.currentNode as Text);
    }

    textNodes.forEach((node) => {
        const translated = translatePreservingWhitespace(node.nodeValue ?? "", language);
        if (translated !== node.nodeValue) {
            node.nodeValue = translated;
        }
    });

    document
        .querySelectorAll<HTMLElement>("[placeholder], [aria-label], [title]")
        .forEach((element) => {
            ["placeholder", "aria-label", "title"].forEach((attribute) => {
                const value = element.getAttribute(attribute);
                if (!value) return;
                const translated = translatePreservingWhitespace(value, language);
                if (translated !== value) {
                    element.setAttribute(attribute, translated);
                }
            });
        });
}

function translatePreservingWhitespace(value: string, language: Language) {
    const leadingWhitespace = value.match(/^\s*/)?.[0] ?? "";
    const trailingWhitespace = value.match(/\s*$/)?.[0] ?? "";
    const normalized = value.trim().replace(/\s+/g, " ");
    if (!normalized) return value;

    const dictionary = language === "sq" ? globalTextTranslations : reverseGlobalTextTranslations;
    const translated = dictionary[normalized];
    if (!translated) return value;

    return `${leadingWhitespace}${translated}${trailingWhitespace}`;
}

export function useLanguage() {
    const context = useContext(LanguageContext);
    if (!context) {
        throw new Error("useLanguage must be used within LanguageProvider");
    }
    return context;
}
